<?php

// for loop - repeat some code a certain # of times.

for ($i = 0; $i < 5; $i++) {
    echo "Hello <br>";
    echo $i . "<br>";
}


for ($i = 0; $i < 50; $i += 3) {
    echo "Hello <br>";
    echo $i . "<br>";
}

for ($i = 2; $i < 100; $i += 2) {
    echo "Hello <br>";
    echo $i . "<br>";
}

for ($i = 10; $i > 0; $i--) {
    echo $i . "<br>";
}
